package Simulator;

public class ForestParams {
    public int forestSize;
    public int forestDensity;
    public int treeBC;
    public int grassBC;
    public int ashBC;

    public ForestParams(int forestSize, int forestDensity, int treeBC, int grassBC, int ashBC){
        this.forestSize = forestSize;
        this.forestDensity = forestDensity;
        this.treeBC = treeBC;
        this.grassBC = grassBC;
        this.ashBC = ashBC;
    }
}
